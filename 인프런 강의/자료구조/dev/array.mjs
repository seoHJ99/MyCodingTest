// 배열
// 배열이란 연속된 메모리 주소에 저장되는 자료구조
// 운영체제는 배열의 시작 위치만 기억하고 있음.
// 배열 중간값을 바로 참조하는것이 가능하기에 읽기가 빠름.
// 반대로 연속된 주소 공간에 데이터를 할당하기에 배열의 크기를 늘리거나
// 줄이는데는 불리함. 따라서 제거, 추가가 느림.
// 데이터의 크기를 예측할수 없기에 메모리 낭비가 발생할수 있음.
// O(n)의 참조 속도
// O(n)의 삽입, 삭제

